                String jogos_inseridos = jTextArea1.getText() + "ID Item: " + info_jogo.getString("ID") + " | NOME: " + info_jogo.getString("NOME") + " | PREÃ‡O: " + info_jogo.getString("PRECO") + "|\n\n";
