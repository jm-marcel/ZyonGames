    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Conexao C = new Conexao();

        if (jTextField1.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Insira o ID do produto que deseja excluir!", "Comprados", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                boolean flag = false;
                
                int produto = Integer.parseInt(jTextField1.getText().trim());
                
                ResultSet rs = C.busca_comprados(user);
                
                while (rs.next()) {
                    if (rs.getString("ID").equals(jTextField1.getText().trim())) {
                        flag = true;
                    }
                }
                if (flag) {
                    if (C.deletarComprados(user, produto)) {
                        JOptionPane.showMessageDialog(rootPane, "Produto removido com sucesso!", "Comprados", JOptionPane.INFORMATION_MESSAGE);
                        dispose();
                        Tela_Comprados TC = new Tela_Comprados();
                        TC.informacoes(user);
                        TC.logado(user);
                        TC.setLocationRelativeTo(null);
                        TC.show();
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Erro ao remover produto!", "Comprados", JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(rootPane, "O ID nÃ£o corresponde a nenhum item no carrinho", "Carrinho", JOptionPane.ERROR_MESSAGE);
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(rootPane, "ID invÃ¡lido!", "Comprados", JOptionPane.ERROR_MESSAGE);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, "Ops! Ocorreu um erro!", "Comprados", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

