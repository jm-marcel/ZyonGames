    public void informacoes(String USUARIO) {
        try {
            Conexao C = new Conexao();
            
            ResultSet user = C.mostra_usuario(USUARIO);
            ResultSet nome = C.mostra_nome(USUARIO);
            ResultSet senha = C.mostra_senha(USUARIO);
            ResultSet email = C.mostra_email(USUARIO);
            
            jTextField1.setEditable(false); // Usuário
            jTextField5.setEditable(false); // Nome
            jTextField6.setEditable(false); // Senha
            jTextField7.setEditable(false); // E-Mail
            
            while (user.next()) {
                jTextField1.setText(user.getString("USUARIO")); // Usuário
            }
            while (nome.next()) {
                jTextField5.setText(nome.getString("NOME")); // Nome
            }
            while (senha.next()) {
                jTextField6.setText(senha.getString("SENHA")); // Senha
            }
            while (email.next()) {
                jTextField7.setText(email.getString("EMAIL")); // E-Mail
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Ops! Algo deu errado!" + e);
        }
    }

