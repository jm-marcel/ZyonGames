    // Botão ALTERAR DADOS
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (jButton1.getText().equals("ALTERAR DADOS")) {
            jTextField1.setEditable(false); // Usuário
            jTextField5.setEditable(true); // Nome
            jTextField6.setEditable(true); // Senha
            jTextField7.setEditable(true); // E-Mail
            jButton1.setText("SALVAR");
            jButton2.setText("APAGAR");
        } else if (jButton1.getText().equals("SALVAR")) {
            Conexao C = new Conexao();

            String nome = jTextField5.getText().trim();
            String usuario = jTextField1.getText().trim();
            String senha = jTextField6.getText().trim();
            String email = jTextField7.getText().trim();
            
            if (nome.length() > 0 && usuario.length() > 0 && senha.length() > 0 && email.length() > 0) {
                if (C.atualizar(nome, usuario, senha, email)) {
                    JOptionPane.showMessageDialog(rootPane, "Dados atualizados com sucesso!");
                    jTextField1.setEditable(false); // Usuário
                    jTextField5.setEditable(false); // Nome
                    jTextField6.setEditable(false); // Senha
                    jTextField7.setEditable(false); // E-Mail 
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Não foi possível atualizar os dados!");
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Não deixe campo(s) em branco!");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    // Botão EXCLUIR
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (jButton2.getText().equals("APAGAR")) {
            jTextField5.setText("");
            jTextField6.setText("");
            jTextField7.setText("");
        } else {
            int resposta;
            resposta = JOptionPane.showConfirmDialog(rootPane, "Deseja relmente excluir seu perfil?");
            if (resposta == JOptionPane.YES_OPTION) {
                Conexao C = new Conexao();
                try {
                    String usuario = jTextField1.getText();
                    if (C.deletar(usuario)) {
                        JOptionPane.showMessageDialog(rootPane, "Dados deletados com sucesso!");
                        dispose();
                        Tela1_LC T = new Tela1_LC();
                        T.setLocationRelativeTo(null);
                        T.limpa_tela();
                        T.show();
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Dados não deletados!");
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(rootPane, "Insira o Usuário corretamente!" + e);
                }
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed
