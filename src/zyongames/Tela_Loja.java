    // Botão COMPRAR
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Conexao C = new Conexao();

        if (jTextField2.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Insira o ID de um produto para comprá-lo!", "Loja", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                boolean flag = false;
                int produto = Integer.parseInt(jTextField2.getText().trim());
                ResultSet rs = C.buscaIDJogos();
                while (rs.next()) {
                    if (rs.getString("ID").equals(jTextField2.getText().trim())) {
                        flag = true;
                    }
                }
                if (flag) {
                    if (C.insereComprados(user, produto)) {
                        JOptionPane.showMessageDialog(rootPane, "Produto adquirido com sucesso!");
                        C.deletarCarrinho(user, produto);
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Você já adquiriu esse produto!");
                    }
                } else {
                    JOptionPane.showMessageDialog(rootPane, "O ID inserido não corresponde a nenhum de nossos jogos!");
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(rootPane, "ID inválido!", "Loja", JOptionPane.ERROR_MESSAGE);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, "Ops! Ocorreu um erro!", "Loja", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

