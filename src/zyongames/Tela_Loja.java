    // Buscar por NOME
    private void jRadioButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButton2ActionPerformed
        // TODO add your handling code here:
        Conexao C = new Conexao();
        String nome = jTextField1.getText();

        if (nome.trim().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Insira um nome no campo de busca!");
        } else {
            try {
                ResultSet rs;
                rs = C.buscaJogosNome(nome);
                // Criando lista de String
                String[] ModeloTabela = new String[]{"ID", "NOME", "CATEGORIA", "FORNECEDOR", "PRECO"};
                // Add Cabeçalho ao modelo padrão
                DefaultTableModel modelo;
                modelo = new DefaultTableModel(null, ModeloTabela);
                // Jogando o molde na parte "física"
                jTable1.setModel(modelo);
                // Manipulando dados do ResultSet
                while (rs.next()) {
                    modelo.addRow(new String[]{rs.getString("ID"), rs.getString("NOME"), rs.getString("CATEGORIA"), rs.getString("FORNECEDOR"), rs.getString("PRECO")});
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Erro na busca!" + e);
            }
        }
    }//GEN-LAST:event_jRadioButton2ActionPerformed
